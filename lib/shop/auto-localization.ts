const TEXT_SANITIZERS: Array<[RegExp, string]> = [
  [/â€“/g, "-"],
  [/â€”/g, "-"],
  [/â€/g, "\""],
  [/â€œ/g, "\""],
  [/â€˜/g, "'"],
  [/â€¢/g, " / "],
  [/â†’/g, " -> "],
  [/ðŸ“/g, ""],
  [/ðŸ“Š/g, ""],
  [/\*\*/g, ""],
];

export function sanitizeLocalizedText(value: string | undefined | null) {
  let nextValue = String(value ?? "");

  for (const [pattern, replacement] of TEXT_SANITIZERS) {
    nextValue = nextValue.replace(pattern, replacement);
  }

  return nextValue.replace(/\r/g, "").trim();
}

export function autoTranslateText(value: string | undefined | null, locale: Locale) {
  const sanitized = sanitizeLocalizedText(value);

  if (!sanitized) {
    return "";
  }

  const exactTranslation = EXACT_TEXT_TRANSLATIONS[sanitized]?.[locale];

  if (exactTranslation) {
    return exactTranslation;
  }

  let translated = sanitized;

  for (const [pattern, replacement] of LOCALE_SPECIFIC_REPLACEMENTS[locale]) {
    translated = translated.replace(pattern, replacement);
  }

  return translated.replace(/[ \t]{2,}/g, " ").trim();
}

export function autoTranslateList(values: string[] | undefined, locale: Locale) {
  if (!values?.length) {
    return [];
  }

  const translatedValues = values
    .map((value) => autoTranslateText(value, locale))
    .filter(Boolean);

  return Array.from(new Set(translatedValues));
}
